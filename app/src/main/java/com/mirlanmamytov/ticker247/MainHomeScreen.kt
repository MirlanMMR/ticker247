 * 3. isTrending    ÔÇö ĐéđÁđ╝đ░ Đüđżđ▓đ┐đ░đ┤đ░đÁĐé Đü ĐéđÁđ╝ ĐçĐéđż đ┐ĐÇĐĆđ╝đż ĐüđÁđ╣Đçđ░Đü đŞĐëĐâĐé đ▓ Google KG/RU.
 *
 * đśĐéđżđ│đżđ▓Đőđ╣ score đŞĐüđ┐đżđ╗ĐîđĚĐâđÁĐéĐüĐĆ đ┐ĐÇđŞ ĐüđżĐÇĐéđŞĐÇđżđ▓đ║đÁ đ▓đ╝đÁĐüĐéđÁ Đü đ┐ĐÇđŞđżĐÇđŞĐéđÁĐéđżđ╝ đŞ Đüđ▓đÁđÂđÁĐüĐéĐîĐÄ.
 */
private fun popularityScore(item: NewsItem): Int {
    var score = 0
    // 1. Google Trends KG ÔÇö Đüđ░đ╝Đőđ╣ ĐüđŞđ╗ĐîđŻĐőđ╣ ĐüđŞđ│đŻđ░đ╗: ĐéđÁđ╝đ░ đ░đ║ĐéĐâđ░đ╗ĐîđŻđ░ đ┐ĐÇĐĆđ╝đż ĐüđÁđ╣Đçđ░Đü đ▓ ĐÇđÁđ│đŞđżđŻđÁ
    if (item.isTrending) score += 60
    // 2. đčĐÇđżĐüđ╝đżĐéĐÇĐő Telegram ÔÇö ĐÇđÁđ░đ╗ĐîđŻĐőđ╣ đŞđŻĐéđÁĐÇđÁĐü đ╗ĐÄđ┤đÁđ╣: đ╗đżđ│đ░ĐÇđŞĐäđ╝đŞĐçđÁĐüđ║đ░ĐĆ Đłđ║đ░đ╗đ░
    //    1KÔćĺ10, 10KÔćĺ20, 100KÔćĺ30, 1MÔćĺ40 đżĐçđ║đżđ▓
    if (item.telegramViews > 0) {
        score += (Math.log10(item.telegramViews.toDouble()) * 10).toInt().coerceIn(0, 40)
    }
    // 3. đÜĐÇđżĐüĐü-đŞĐüĐéđżĐçđŻđŞđ║đżđ▓Đőđ╣ ĐüđŞđ│đŻđ░đ╗: đ║đ░đÂđ┤Đőđ╣ đ┤đżđ┐đżđ╗đŻđŞĐéđÁđ╗ĐîđŻĐőđ╣ đŞĐüĐéđżĐçđŻđŞđ║ +20 đżĐçđ║đżđ▓
    score += (item.sourceCount - 1) * 20
    return score
}

fun sortItems(items: List<NewsItem>, cat: String): List<NewsItem> {
    val maxAge = 30L * 24 * 60 * 60 * 1000  // 30 đ┤đŻđÁđ╣ đ╝đ░đ║ĐüđŞđ╝Đâđ╝
    val now = System.currentTimeMillis()

    // đÜđ░ĐéđÁđ│đżĐÇđŞđŞ đ║đżĐéđżĐÇĐőđÁ đŁđĽ đ┐đżđ║đ░đĚĐőđ▓đ░đÁđ╝ đ▓ đżđ▒ĐëđÁđ╣ đ╗đÁđŻĐéđÁ (Đâ đŻđŞĐů Đüđ▓đżđŞ đ┐đ╗đŞĐéđ║đŞ)
    val excludedFromFeed = setOf("CURRENCY", "CRYPTO")

    val filtered = when (cat) {
        "ALL" -> items
            .filter { it.category !in excludedFromFeed }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
            .filter { it.title.length > 15 }
            .map { enrichCategory(it) }
        "URGENT" -> items
            .filter { it.category == "URGENT" || it.priority >= 3 }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
        else -> items
            .filter { it.category == cat }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
    }

    // ÔöÇÔöÇ đÜĐÇđżĐüĐü-đŞĐüĐéđżĐçđŻđŞđ║đżđ▓Đőđ╣ ĐÇđÁđ╣ĐéđŞđŻđ│ ÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇ
    // đíĐçđŞĐéđ░đÁđ╝ Đüđ║đżđ╗Đîđ║đż ĐÇđ░đĚ đżđ┤đŻđ░ ĐéđÁđ╝đ░ đ▓ĐüĐéĐÇđÁĐçđ░đÁĐéĐüĐĆ ĐüĐÇđÁđ┤đŞ ĐÇđ░đĚđŻĐőĐů đŞĐüĐéđżĐçđŻđŞđ║đżđ▓.
    // đĺđ░đÂđŻđż đ┤đÁđ╗đ░ĐéĐî đöđ× đ┤đÁđ┤Đâđ┐đ░ ÔÇö đ┐đżĐüđ╗đÁ đ┤đÁđ┤Đâđ┐đ░ đżĐüĐéđ░đŻđÁĐéĐüĐĆ Đéđżđ╗Đîđ║đż đżđ┤đŻđ░ đĚđ░đ┐đŞĐüĐî.
    val titleCountMap = mutableMapOf<String, Int>()
    val titleViewsMap = mutableMapOf<String, Int>()  // ĐüĐâđ╝đ╝đŞĐÇĐâđÁđ╝ đ┐ĐÇđżĐüđ╝đżĐéĐÇĐő đ┐đż ĐéđÁđ╝đÁ
    val titleTrendMap = mutableMapOf<String, Boolean>()
    filtered.forEach { item ->
        val tk = titleKey(item.title)
        titleCountMap[tk] = (titleCountMap[tk] ?: 0) + 1
        titleViewsMap[tk] = (titleViewsMap[tk] ?: 0) + item.telegramViews
        if (item.isTrending) titleTrendMap[tk] = true
    }

    // đ×đ▒đżđ│đ░Đëđ░đÁđ╝ items đ░đ│ĐÇđÁđ│đŞĐÇđżđ▓đ░đŻđŻĐőđ╝đŞ đ┤đ░đŻđŻĐőđ╝đŞ
    val enriched = filtered.map { item ->
        val tk = titleKey(item.title)
        val count   = titleCountMap[tk] ?: 1
        val views   = titleViewsMap[tk] ?: item.telegramViews
        val trend   = titleTrendMap[tk] ?: item.isTrending
        if (count != item.sourceCount || views != item.telegramViews || trend != item.isTrending)
            item.copy(sourceCount = count, telegramViews = views, isTrending = trend)
        else item
    }

    // đíđżĐÇĐéđŞĐÇĐâđÁđ╝ đ┐đÁĐÇđÁđ┤ đ┤đÁđ┤Đâđ┐đżđ╝ ÔÇö đ╗ĐâĐçĐłđŞđ╣ ĐŹđ║đĚđÁđ╝đ┐đ╗ĐĆĐÇ (Đü ĐäđżĐéđż/đ▓đŞđ┤đÁđż) đżĐüĐéđ░ĐĹĐéĐüĐĆ
    val preSorted = enriched.sortedWith(
        compareByDescending<NewsItem> {
            when {
                it.isVideo          -> 4
                it.imageUrl != null -> 3
                cat == "ALL" && it.category == "KG" -> 2
                else                -> 0
            }
        }.thenByDescending { it.priority * 1000L + it.publishedAt / 1000L }
    )

    // đöđÁđ┤Đâđ┐đ╗đŞđ║đ░ĐćđŞĐĆ: đ┐đż URL, đ┐đżĐéđżđ╝ đ┐đż đŻđżĐÇđ╝đ░đ╗đŞđĚđżđ▓đ░đŻđŻđżđ╝Đâ đĚđ░đ│đżđ╗đżđ▓đ║Đâ
    val seenUrls   = mutableSetOf<String>()
    val seenTitles = mutableSetOf<String>()
    val deduped = preSorted.filter { item ->
        val url = item.url.ifEmpty { item.title }
        val tk  = titleKey(item.title)
        if (url in seenUrls || (tk.length >= 30 && tk in seenTitles)) return@filter false
        seenUrls   += url
        if (tk.length >= 30) seenTitles += tk
        true
    }

    // ÔöÇÔöÇ đĄđŞđŻđ░đ╗ĐîđŻđ░ĐĆ ĐüđżĐÇĐéđŞĐÇđżđ▓đ║đ░ Đü popularity score ÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇ
    val sorted = deduped.sortedWith(
        compareByDescending<NewsItem> {
            // đčĐÇđżĐçđŞĐéđ░đŻđŻĐőđÁ đ▓ĐüđÁđ│đ┤đ░ đ▓đŻđŞđĚĐâ (Instagram-đ╗đżđ│đŞđ║đ░)
            if (DataBridge.isSeen(it.url)) return@compareByDescending Long.MIN_VALUE
            // đĹđ░đĚđżđ▓Đőđ╣ score: đ┐ĐÇđŞđżĐÇđŞĐéđÁĐé + Đüđ▓đÁđÂđÁĐüĐéĐî (đ▓ Đçđ░Đüđ░Đů, max 72h) + popularity
            val freshness = ((72 * 60 * 60 * 1000L - (now - it.publishedAt).coerceAtLeast(0L)) / 3_600_000L).coerceIn(0L, 72L)
            val pop  = popularityScore(it).toLong()
            val base = when {
                cat == "ALL" && it.category == "KG"   -> 200L
                cat == "ALL" && it.category == "GOOD" -> 120L  // ĐůđżĐÇđżĐłđŞđÁ đŻđżđ▓đżĐüĐéđŞ đ┐đżđ┤đŻđŞđ╝đ░đÁđ╝ ÔÇö đ▒đ░đ╗đ░đŻĐü đŻđ░ĐüĐéĐÇđżđÁđŻđŞĐĆ
                cat == "ALL" && it.category == "TOURS"-> 80L   // ĐéĐâĐÇđŞđĚđ╝/đ▓đ┤đżĐůđŻđżđ▓đÁđŻđŞđÁ ĐéđżđÂđÁ đ▓đ▓đÁĐÇĐů
                it.isVideo          -> 150L
                it.imageUrl != null -> 100L
                else                -> 0L
            }
            // đśĐéđżđ│đżđ▓Đőđ╣ ĐÇđ░đŻđ│: priority (0-3) * 1000 + đ┐đżđ┐Đâđ╗ĐĆĐÇđŻđżĐüĐéĐî + Đüđ▓đÁđÂđÁĐüĐéĐî + ĐéđŞđ┐
            it.priority * 1000L + pop + freshness * 2 + base
        }
    )

    // đ×đ│ĐÇđ░đŻđŞĐçđŞđ▓đ░đÁđ╝ đ▓đŞđ┤đÁđż: đŻđÁ đ▒đżđ╗đÁđÁ 2 đ▓ đ╗đÁđŻĐéđÁ, đŻđÁ đ▒đżđ╗đÁđÁ 1 đŻđ░ ĐéđÁđ╝Đâ
    // đóđÁđ╝đ░ đżđ┐ĐÇđÁđ┤đÁđ╗ĐĆđÁĐéĐüĐĆ đ┐đż đ┐đÁĐÇđÁĐüđÁĐçđÁđŻđŞĐÄ đĚđŻđ░ĐçđŞđ╝ĐőĐů Đüđ╗đżđ▓ (>4 đ▒Đâđ║đ▓) ÔÇö đ╗đżđ▓đŞĐé "đžđÁđ╗đżđ▓đÁđ║-đ┐đ░Đâđ║" đ▓đż đ▓ĐüđÁĐů đ▓đ░ĐÇđŞđ░đŻĐéđ░Đů
    val finalList = if (cat == "ALL" || cat == "URGENT") {
        val seenTopicWords = mutableSetOf<String>()
        var videoCount = 0
        sorted.filter { item ->
            if (!item.isVideo) return@filter true
            if (videoCount >= 2) return@filter false
            val words = item.title.lowercase()
                .split(Regex("[\\s\\-:,.!?\"']+"))
                .filter { it.length > 4 }
                .toSet()
            if (words.any { it in seenTopicWords }) return@filter false
            seenTopicWords += words
            videoCount++
            true
        }
    } else sorted

    return if (cat == "ALL") interleave(finalList) else finalList
}

/** đčđÁĐÇđÁđ╝đÁĐłđŞđ▓đ░đÁĐé Đüđ┐đŞĐüđżđ║ ĐçĐéđżđ▒Đő đżđ┤đŞđŻđ░đ║đżđ▓ĐőđÁ đŞĐüĐéđżĐçđŻđŞđ║đŞ/đ║đ░ĐéđÁđ│đżĐÇđŞđŞ đŻđÁ Đłđ╗đŞ đ┐đżđ┤ĐÇĐĆđ┤ */
private fun interleave(items: List<NewsItem>): List<NewsItem> {
    // đôĐÇĐâđ┐đ┐đŞĐÇĐâđÁđ╝ đ┐đż đ║đ░ĐéđÁđ│đżĐÇđŞđŞ
    val byCategory = items.groupBy { it.category }.values.map { it.toMutableList() }
    val result = mutableListOf<NewsItem>()
    var hasMore = true
    while (hasMore) {
        hasMore = false
        for (group in byCategory) {
            if (group.isNotEmpty()) {
                result.add(group.removeAt(0))
                hasMore = true
            }
        }
    }
    return result
}

