enum class TileSize { LARGE, MEDIUM, SMALL }

// ÔöÇÔöÇÔöÇ đľđŞđ▓đ░ĐĆ đ┐đ╗đŞĐéđ║đ░ Đü Lumia flip-đ░đŻđŞđ╝đ░ĐćđŞđÁđ╣ ÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇÔöÇ

@OptIn(androidx.compose.animation.ExperimentalAnimationApi::class)
@Composable
fun LiveTile(
    item: NewsItem,
    size: TileSize,
    textColor: Color,
    subColor: Color,
    onClick: () -> Unit
) {
    val style    = newsItemStyle(item.category)
    val isSeen   = DataBridge.isSeen(item.url)   // đ┐ĐÇđżĐçđŞĐéđ░đŻđŻĐőđÁ đ┐ĐÇđŞđ│đ╗ĐâĐłđ░đÁđ╝
    // og:image đ┐đżđ┤đ│ĐÇĐâđÂđ░đÁĐéĐüĐĆ đ╗đÁđŻđŞđ▓đż đÁĐüđ╗đŞ đ▓ RSS ĐäđżĐéđż đŻđÁ đ▒Đőđ╗đż
    var resolvedImageUrl by remember(item.url) { mutableStateOf(item.imageUrl) }
    var showPhoto by remember(item.url) { mutableStateOf(false) }
    val flipInterval = remember(item.url) { (3500L..9000L).random() }

    // đĄđżđŻđżđ▓đ░ĐĆ đĚđ░đ│ĐÇĐâđĚđ║đ░ ĐäđżĐéđż: og:image Ôćĺ Wikipedia Ôćĺ đ║đ░ĐéđÁđ│đżĐÇđŞđ╣đŻĐőđ╣ fallback
    LaunchedEffect(item.url) {
        if (item.imageUrl == null) {
            // đöđ╗ĐĆ đŻđżđ▓đżĐüĐéđÁđ╣ Đü URL ÔÇö đ┐ĐÇđżđ▒ĐâđÁđ╝ og:image ĐüĐéĐÇđ░đŻđŞĐćĐő
            // đöđ╗ĐĆ Telegram (đŻđÁĐé URL đŞđ╗đŞ t.me) ÔÇö ĐüĐÇđ░đĚĐâ Wikipedia đ┐đż đĚđ░đ│đżđ╗đżđ▓đ║Đâ
            val fetched = com.mirlanmamytov.ticker247.network.OgImageFetcher.fetch(
                articleUrl = item.url,
                title = item.title,
                category = item.category
            )
            if (fetched != null) resolvedImageUrl = fetched
        }
        // đÉđŻđŞđ╝đ░ĐćđŞĐĆ: ĐüĐéđ░ĐÇĐéĐâđÁĐé đ║đ░đ║ Đéđżđ╗Đîđ║đż ĐäđżĐéđż đÁĐüĐéĐî (ĐüĐÇđ░đĚĐâ đŞđ╗đŞ đ┐đżĐüđ╗đÁ đ┐đżđ┤đ│ĐÇĐâđĚđ║đŞ)
        while (isActive) {
            if (resolvedImageUrl != null) {
                kotlinx.coroutines.delay(flipInterval)
                showPhoto = !showPhoto
            } else {
                kotlinx.coroutines.delay(1000L) // đÂđ┤ĐĹđ╝ đ┐đżĐĆđ▓đ╗đÁđŻđŞĐĆ ĐäđżĐéđż
            }
        }
    }
    val hasPhoto = resolvedImageUrl != null

    val height = when (size) {
        TileSize.LARGE  -> 190.dp
        TileSize.MEDIUM -> 130.dp
        TileSize.SMALL  -> 90.dp
    }
    val corner = when (size) {
        TileSize.LARGE  -> 14.dp
        TileSize.MEDIUM -> 12.dp
        TileSize.SMALL  -> 10.dp
    }

    Box(
        Modifier.fillMaxWidth().height(height)
            .shadow(
                elevation = if (size == TileSize.LARGE) 6.dp else 3.dp,
                shape = RoundedCornerShape(corner),
                ambientColor = style.accent.copy(if (isSeen) 0.05f else 0.15f),
                spotColor = style.accent.copy(if (isSeen) 0.06f else 0.18f)
            )
            .clip(RoundedCornerShape(corner))
            .graphicsLayer { alpha = if (isSeen) 0.55f else 1f }  // đ┐ĐÇđżĐçđŞĐéđ░đŻđŻĐőđÁ đ┐ĐÇđŞđ│đ╗ĐâĐłđÁđŻĐő
            .clickable(onClick = onClick)
    ) {
        androidx.compose.animation.AnimatedContent(
            targetState = showPhoto && hasPhoto,
            transitionSpec = {
                (androidx.compose.animation.slideInVertically { -it } +
                 androidx.compose.animation.fadeIn(tween(400)))
                    .togetherWith(
                 androidx.compose.animation.slideOutVertically { it } +
                 androidx.compose.animation.fadeOut(tween(400)))
            },
            label = "flip_${item.url}"
        ) { isPhoto ->
            if (isPhoto) {
                Box(Modifier.fillMaxSize()) {
                    AsyncImage(
                        model = resolvedImageUrl, contentDescription = null,
                        contentScale = ContentScale.Crop, modifier = Modifier.fillMaxSize(),
                        onError = { showPhoto = false }
