    val lang = java.util.Locale.getDefault().language
    val cyrillicLangs = setOf("ru", "ky", "uk", "be", "bg", "sr", "mk")
    if (lang !in cyrillicLangs) return

    val context = LocalContext.current
    var slideIdx by remember { mutableStateOf(0) }

    // –Р–≤—В–Њ–њ–µ—А–µ–Ї–ї—О—З–µ–љ–Є–µ вАФ —В–∞–Ї–Њ–є –ґ–µ –Є–љ—В–µ—А–≤–∞–ї –Ї–∞–Ї —Г –љ–Њ–≤–Њ—Б—В–љ—Л—Е –њ–ї–Є—В–Њ–Ї
    LaunchedEffect(Unit) {
        while (isActive) {
            kotlinx.coroutines.delay(6000L)
            slideIdx = (slideIdx + 1) % PROMO_SLIDES.size
        }
    }

