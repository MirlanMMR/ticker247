fun timeAgo(timestamp: Long): String {
    val diff = System.currentTimeMillis() - timestamp
    val min  = diff / 60_000
    val h    = min / 60
    return when {
        min < 1  -> "—В–Њ–ї—М–Ї–Њ —З—В–Њ"
        min < 60 -> "$min –Љ–Є–љ"
        h < 24   -> "$h —З"
        else     -> "${h / 24} –і–љ"
    }
}

