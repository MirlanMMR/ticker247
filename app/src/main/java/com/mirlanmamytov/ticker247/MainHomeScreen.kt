                DataBridge.pendingArticleUrl = ""
            }
            // Р•СҒР»Рё newsItemsState РҝСғСҒСӮ вҖ” LaunchedEffect РҝРөСҖРөР·Р°РҝСғСҒСӮРёСӮСҒСҸ РәРҫРіРҙР° РҪРҫРІРҫСҒСӮРё РҝСҖРёРҙСғСӮ
        }
