    listOf(TileSize.MEDIUM, TileSize.MEDIUM),
    listOf(TileSize.MEDIUM, TileSize.MEDIUM),
)

@Composable
fun NewsTileGrid(
    category: String = "ALL",
    textColor: Color,
    subColor: Color,
    onOpenTikTok: (List<NewsItem>, Int) -> Unit
) {
    val allItems by DataBridge.newsItemsFlow.collectAsState()
    val sorted = remember(allItems, category) {
        // –Ш—Б–Ї–ї—О—З–∞–µ–Љ –љ–Њ–≤–Њ—Б—В–Є, —Г–ґ–µ –њ–Њ–Ї–∞–Ј–∞–љ–љ—Л–µ –≤ hero-–Ї–∞—А—Г—Б–µ–ї–Є (—В–Њ–ї—М–Ї–Њ –і–ї—П –≥–ї–∞–≤–љ–Њ–є)
        val heroUrls = if (category == "ALL")
            selectHeroItems(allItems).map { it.url.ifEmpty { it.title } }.toSet()
        else emptySet()
        sortItems(allItems, category).filter { (it.url.ifEmpty { it.title }) !in heroUrls }
    }

    if (allItems.isEmpty()) return  // HomeContent —Г–ґ–µ –њ–Њ–Ї–∞–Ј–∞–ї SplashLoadingScreen

    // –°–ї—Г—З–∞–є–љ—Л–є –њ–Њ—А—П–і–Њ–Ї –њ–∞—В—В–µ—А–љ–Њ–≤ вАФ seed –Њ—В –і–љ—П, —Б—В–∞–±–Є–ї–µ–љ –≤ —А–∞–Љ–Ї–∞—Е –і–љ—П
    val shuffledPatterns = remember {
        val daySeed = System.currentTimeMillis() / (24 * 60 * 60 * 1000)
        TILE_PATTERNS.shuffled(java.util.Random(daySeed))
    }

    // –Ф–ї—П –≤–Ї–ї–∞–і–Ї–Є ALL вАФ –њ–Њ–Ї–∞–Ј—Л–≤–∞–µ–Љ –і–≤–µ —Б–µ–Ї—Ж–Є–Є: –Ь–µ—Б—В–љ—Л–µ –Є –Ь–Є—А–Њ–≤—Л–µ
    // YouTube/VIRAL –±–µ–Ј scope –Є–і—Г—В –≤ local (–≤–Є—А—Г—Б—Л —А–µ–≥–Є–Њ–љ–∞–ї—М–љ—Л–µ –њ–Њ –њ—А–Є—А–Њ–і–µ)
    if (category == "ALL") {
        val localItems = sorted.filter { it.scope == "local" || (it.scope == "world" && it.isVideo) }
        val worldItems = sorted.filter { it.scope == "world" && !it.isVideo }
        Column(Modifier.fillMaxWidth()) {
            if (localItems.isNotEmpty()) {
                ScopeSectionHeader(emoji = "рЯУН", title = "–Ь–µ—Б—В–љ—Л–µ")
                NewsTileBlock(localItems, textColor, subColor, shuffledPatterns, onOpenTikTok, adOffset = 0)
            }
            if (worldItems.isNotEmpty()) {
                ScopeSectionHeader(emoji = "рЯМН", title = "–Ь–Є—А–Њ–≤—Л–µ")
                NewsTileBlock(worldItems, textColor, subColor, shuffledPatterns, onOpenTikTok, adOffset = 3)
            }
        }
        return
    }

    Column(Modifier.fillMaxWidth().padding(horizontal = 10.dp)) {
        var itemIdx = 0
        var rowCount = 0
        while (itemIdx < sorted.size) {
            // –†—П–і 4: –њ–µ—А–≤—Л–є —Б–ї–Њ—В –Ї–Њ–љ—В–µ–Ї—Б—В–љ–Њ–є —А–µ–Ї–ї–∞–Љ—Л (AdMob)
            if (rowCount == 4) {
                ContextualAdSlot()
                Spacer(Modifier.height(8.dp))
            }
            // –†—П–і 7: –њ–∞—А—В–љ—С—А—Б–Ї–Є–є —Б–ї–Њ—В "–†–µ–Ї–ї–∞–Љ–∞ ¬Ј –°–Њ—В—А—Г–і–љ–Є—З–µ—Б—В–≤–Њ"
            if (rowCount == 7) {
                PromoSlot()
                Spacer(Modifier.height(8.dp))
            }
            // –†—П–і 10: –≤—В–Њ—А–Њ–є —Б–ї–Њ—В –Ї–Њ–љ—В–µ–Ї—Б—В–љ–Њ–є —А–µ–Ї–ї–∞–Љ—Л (AdMob)
            if (rowCount == 10) {
                ContextualAdSlot()
                Spacer(Modifier.height(8.dp))
            }

            val pattern = shuffledPatterns[rowCount % shuffledPatterns.size]
            val available = sorted.size - itemIdx

            // –Ф–Њ—Б—В–∞—В–Њ—З–љ–Њ –ї–Є –љ–Њ–≤–Њ—Б—В–µ–є –і–ї—П —Н—В–Њ–≥–Њ –њ–∞—В—В–µ—А–љ–∞?
            if (available < pattern.size) {
                // –Ф–Њ–±–Є–≤–∞–µ–Љ –Њ—Б—В–∞–≤—И–Є–µ—Б—П –Ї–∞–Ї MEDIUM
                val remaining = sorted.subList(itemIdx, sorted.size)
                if (remaining.isNotEmpty()) {
                    if (remaining.size == 1) {
                        val i0 = itemIdx
                        LiveTile(remaining[0], TileSize.MEDIUM, textColor, subColor) { onOpenTikTok(sorted, i0) }
                    } else {
                        Row(Modifier.fillMaxWidth(), horizontalArrangement = Arrangement.spacedBy(8.dp)) {
                            remaining.forEachIndexed { offset, item ->
                                val capturedIdx = itemIdx + offset
                                Box(Modifier.weight(1f)) {
                                    LiveTile(item, TileSize.MEDIUM, textColor, subColor) { onOpenTikTok(sorted, capturedIdx) }
                                }
                            }
                        }
                    }
                }
                break
            }

            // –Т–Р–Ц–Э–Ю: –Ј–∞—Е–≤–∞—В—Л–≤–∞–µ–Љ itemIdx –≤ val –і–Њ –ї—П–Љ–±–і—Л вАФ –Є–љ–∞—З–µ —В–∞–њ –Њ—В–Ї—А—Л–≤–∞–µ—В –љ–µ —В—Г –љ–Њ–≤–Њ—Б—В—М
            when (pattern.size) {
                1 -> {
                    val i0 = itemIdx
                    LiveTile(sorted[i0], pattern[0], textColor, subColor) { onOpenTikTok(sorted, i0) }
                    itemIdx++
                }
                2 -> {
                    val i0 = itemIdx; val i1 = itemIdx + 1
                    Row(Modifier.fillMaxWidth(), horizontalArrangement = Arrangement.spacedBy(8.dp)) {
                        Box(Modifier.weight(1f)) { LiveTile(sorted[i0], pattern[0], textColor, subColor) { onOpenTikTok(sorted, i0) } }
                        Box(Modifier.weight(1f)) { LiveTile(sorted[i1], pattern[1], textColor, subColor) { onOpenTikTok(sorted, i1) } }
                    }
                    itemIdx += 2
                }
                3 -> {
                    val i0 = itemIdx; val i1 = itemIdx + 1; val i2 = itemIdx + 2
                    Row(Modifier.fillMaxWidth(), horizontalArrangement = Arrangement.spacedBy(8.dp)) {
                        Box(Modifier.weight(1f)) { LiveTile(sorted[i0], pattern[0], textColor, subColor) { onOpenTikTok(sorted, i0) } }
                        Box(Modifier.weight(1f)) { LiveTile(sorted[i1], pattern[1], textColor, subColor) { onOpenTikTok(sorted, i1) } }
                        Box(Modifier.weight(1f)) { LiveTile(sorted[i2], pattern[2], textColor, subColor) { onOpenTikTok(sorted, i2) } }
                    }
                    itemIdx += 3
                }
            }
            Spacer(Modifier.height(8.dp))
            rowCount++
        }
    }
}

