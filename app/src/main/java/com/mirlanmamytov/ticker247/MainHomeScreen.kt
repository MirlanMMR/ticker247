// –Я–∞—В—В–µ—А–љ—Л —А—П–і–Њ–≤: —Б–ї—Г—З–∞–є–љ—Л–є –Љ–Є–Ї—Б (seed –Њ—В –і–љ—П = –Ї–∞–ґ–і—Л–є –і–µ–љ—М –і—А—Г–≥–Њ–є –њ–Њ—А—П–і–Њ–Ї)
private val TILE_PATTERNS = listOf(
    listOf(TileSize.LARGE),
    listOf(TileSize.MEDIUM, TileSize.MEDIUM),
    listOf(TileSize.LARGE),
    listOf(TileSize.MEDIUM, TileSize.MEDIUM),
