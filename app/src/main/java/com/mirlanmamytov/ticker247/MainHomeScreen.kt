    val filtered = when (cat) {
        "ALL" -> items
            .filter { it.category !in excludedFromFeed }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
            .filter { it.title.length > 15 }
            .map { enrichCategory(it) }
        "URGENT" -> items
            .filter { it.category == "URGENT" || it.priority >= 3 }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
        else -> items
            .filter { it.category == cat }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
    }

    // в”Җв”Җ РҡСҖРҫСҒСҒ-РёСҒСӮРҫСҮРҪРёРәРҫРІСӢР№ СҖРөР№СӮРёРҪРі в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    // РЎСҮРёСӮР°РөРј СҒРәРҫР»СҢРәРҫ СҖР°Р· РҫРҙРҪР° СӮРөРјР° РІСҒСӮСҖРөСҮР°РөСӮСҒСҸ СҒСҖРөРҙРё СҖР°Р·РҪСӢС… РёСҒСӮРҫСҮРҪРёРәРҫРІ.
    // Р’Р°Р¶РҪРҫ РҙРөР»Р°СӮСҢ Р”Рһ РҙРөРҙСғРҝР° вҖ” РҝРҫСҒР»Рө РҙРөРҙСғРҝР° РҫСҒСӮР°РҪРөСӮСҒСҸ СӮРҫР»СҢРәРҫ РҫРҙРҪР° Р·Р°РҝРёСҒСҢ.
    val titleCountMap = mutableMapOf<String, Int>()
    val titleViewsMap = mutableMapOf<String, Int>()  // СҒСғРјРјРёСҖСғРөРј РҝСҖРҫСҒРјРҫСӮСҖСӢ РҝРҫ СӮРөРјРө
    val titleTrendMap = mutableMapOf<String, Boolean>()
    filtered.forEach { item ->
        val tk = titleKey(item.title)
        titleCountMap[tk] = (titleCountMap[tk] ?: 0) + 1
        titleViewsMap[tk] = (titleViewsMap[tk] ?: 0) + item.telegramViews
        if (item.isTrending) titleTrendMap[tk] = true
    }

    // РһРұРҫРіР°СүР°РөРј items Р°РіСҖРөРіРёСҖРҫРІР°РҪРҪСӢРјРё РҙР°РҪРҪСӢРјРё
    val enriched = filtered.map { item ->
        val tk = titleKey(item.title)
        val count   = titleCountMap[tk] ?: 1
        val views   = titleViewsMap[tk] ?: item.telegramViews
        val trend   = titleTrendMap[tk] ?: item.isTrending
        if (count != item.sourceCount || views != item.telegramViews || trend != item.isTrending)
            item.copy(sourceCount = count, telegramViews = views, isTrending = trend)
        else item
    }

    // РЎРҫСҖСӮРёСҖСғРөРј РҝРөСҖРөРҙ РҙРөРҙСғРҝРҫРј вҖ” Р»СғСҮСҲРёР№ СҚРәР·РөРјРҝР»СҸСҖ (СҒ С„РҫСӮРҫ/РІРёРҙРөРҫ) РҫСҒСӮР°С‘СӮСҒСҸ
    val preSorted = enriched.sortedWith(
        compareByDescending<NewsItem> {
            when {
                it.isVideo          -> 4
                it.imageUrl != null -> 3
                cat == "ALL" && it.category == "KG" -> 2
                else                -> 0
            }
        }.thenByDescending { it.priority * 1000L + it.publishedAt / 1000L }
    )

    // Р”РөРҙСғРҝР»РёРәР°СҶРёСҸ: РҝРҫ URL, РҝРҫСӮРҫРј РҝРҫ РҪРҫСҖРјР°Р»РёР·РҫРІР°РҪРҪРҫРјСғ Р·Р°РіРҫР»РҫРІРәСғ
    val seenUrls   = mutableSetOf<String>()
    val seenTitles = mutableSetOf<String>()
    val deduped = preSorted.filter { item ->
        val url = item.url.ifEmpty { item.title }
        val tk  = titleKey(item.title)
        if (url in seenUrls || (tk.length >= 30 && tk in seenTitles)) return@filter false
        seenUrls   += url
        if (tk.length >= 30) seenTitles += tk
        true
    }

    // в”Җв”Җ РӨРёРҪР°Р»СҢРҪР°СҸ СҒРҫСҖСӮРёСҖРҫРІРәР° СҒ popularity score в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    val sorted = deduped.sortedWith(
        compareByDescending<NewsItem> {
            // РҹСҖРҫСҮРёСӮР°РҪРҪСӢРө РІСҒРөРіРҙР° РІРҪРёР·Сғ (Instagram-Р»РҫРіРёРәР°)
            if (DataBridge.isSeen(it.url)) return@compareByDescending Long.MIN_VALUE
            // Р‘Р°Р·РҫРІСӢР№ score: РҝСҖРёРҫСҖРёСӮРөСӮ + СҒРІРөР¶РөСҒСӮСҢ (РІ СҮР°СҒР°С…, max 72h) + popularity
            val freshness = ((72 * 60 * 60 * 1000L - (now - it.publishedAt).coerceAtLeast(0L)) / 3_600_000L).coerceIn(0L, 72L)
            val pop  = popularityScore(it).toLong()
            val base = when {
                cat == "ALL" && it.category == "KG"   -> 200L
                cat == "ALL" && it.category == "GOOD" -> 120L  // С…РҫСҖРҫСҲРёРө РҪРҫРІРҫСҒСӮРё РҝРҫРҙРҪРёРјР°РөРј вҖ” РұР°Р»Р°РҪСҒ РҪР°СҒСӮСҖРҫРөРҪРёСҸ
                cat == "ALL" && it.category == "TOURS"-> 80L   // СӮСғСҖРёР·Рј/РІРҙРҫС…РҪРҫРІРөРҪРёРө СӮРҫР¶Рө РІРІРөСҖС…
                it.isVideo          -> 150L
                it.imageUrl != null -> 100L
                else                -> 0L
            }
            // РҳСӮРҫРіРҫРІСӢР№ СҖР°РҪРі: priority (0-3) * 1000 + РҝРҫРҝСғР»СҸСҖРҪРҫСҒСӮСҢ + СҒРІРөР¶РөСҒСӮСҢ + СӮРёРҝ
            it.priority * 1000L + pop + freshness * 2 + base
        }
    )

    // РһРіСҖР°РҪРёСҮРёРІР°РөРј РІРёРҙРөРҫ: РҪРө РұРҫР»РөРө 2 РІ Р»РөРҪСӮРө, РҪРө РұРҫР»РөРө 1 РҪР° СӮРөРјСғ
    // РўРөРјР° РҫРҝСҖРөРҙРөР»СҸРөСӮСҒСҸ РҝРҫ РҝРөСҖРөСҒРөСҮРөРҪРёСҺ Р·РҪР°СҮРёРјСӢС… СҒР»РҫРІ (>4 РұСғРәРІ) вҖ” Р»РҫРІРёСӮ "Р§РөР»РҫРІРөРә-РҝР°СғРә" РІРҫ РІСҒРөС… РІР°СҖРёР°РҪСӮР°С…
    val finalList = if (cat == "ALL" || cat == "URGENT") {
        val seenTopicWords = mutableSetOf<String>()
        var videoCount = 0
        sorted.filter { item ->
            if (!item.isVideo) return@filter true
            if (videoCount >= 2) return@filter false
            val words = item.title.lowercase()
                .split(Regex("[\\s\\-:,.!?\"']+"))
                .filter { it.length > 4 }
                .toSet()
            if (words.any { it in seenTopicWords }) return@filter false
            seenTopicWords += words
            videoCount++
            true
        }
    } else sorted

    return if (cat == "ALL") interleave(finalList) else finalList
}

/** РҹРөСҖРөРјРөСҲРёРІР°РөСӮ СҒРҝРёСҒРҫРә СҮСӮРҫРұСӢ РҫРҙРёРҪР°РәРҫРІСӢРө РёСҒСӮРҫСҮРҪРёРәРё/РәР°СӮРөРіРҫСҖРёРё РҪРө СҲР»Рё РҝРҫРҙСҖСҸРҙ */
private fun interleave(items: List<NewsItem>): List<NewsItem> {
    // Р“СҖСғРҝРҝРёСҖСғРөРј РҝРҫ РәР°СӮРөРіРҫСҖРёРё
    val byCategory = items.groupBy { it.category }.values.map { it.toMutableList() }
    val result = mutableListOf<NewsItem>()
    var hasMore = true
    while (hasMore) {
        hasMore = false
        for (group in byCategory) {
            if (group.isNotEmpty()) {
                result.add(group.removeAt(0))
                hasMore = true
            }
        }
    }
    return result
}

