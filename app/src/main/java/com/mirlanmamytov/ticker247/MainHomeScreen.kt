        ).containsMatchIn(t) -> "LIFE"
        // рЯШК –•–Њ—А–Њ—И–Є–µ –љ–Њ–≤–Њ—Б—В–Є вАФ –њ–Њ–Ј–Є—В–Є–≤–љ—Л–µ —Б–Њ–±—Л—В–Є—П, –і–Њ–±—А—Л–µ –Є—Б—В–Њ—А–Є–Є, –і–Њ—Б—В–Є–ґ–µ–љ–Є—П
        Regex(
            "—Б–њ–∞—Б|–њ–Њ–Љ–Њ–≥|–±–ї–∞–≥–Њ—В–≤–Њ—А–Є—В|–њ–Њ–і–∞—А–Є–ї|—А–µ–Ї–Њ—А–і —Г—Б—В–∞–љ–Њ–≤|–Љ–Є—А–Њ–≤–Њ–є —А–µ–Ї–Њ—А–і|–Њ—В–Ї—А—Л—В.{0,15}(–±–Њ–ї—М–љ–Є—Ж|—И–Ї–Њ–ї|–њ–∞—А–Ї|—Ж–µ–љ—В—А)|" +
            "–≤–њ–µ—А–≤—Л–µ –≤ –Є—Б—В–Њ—А–Є–Є|–і–Њ—Б—В–Є–ґ–µ–љ|–њ—А–Њ—А—Л–≤|–Є–Ј–Њ–±—А–µ—В|–Њ—В–Ї—А—Л—В.{0,10}–ї–µ—З–µ–љ|–њ–Њ–±–µ–і–Є–ї –±–Њ–ї–µ–Ј–љ|" +
            "–і–Њ–±—А.{0,10}(–і–µ–ї|–њ–Њ—Б—В—Г–њ–Њ–Ї|—Б–Њ—Б–µ–і|—З–µ–ї–Њ–≤–µ–Ї)|–њ–Њ–Љ–Њ—Й—М|–≤–Њ–ї–Њ–љ—В—С—А|" +
            "–Є–љ–Њ—Б—В—А–∞–љ.{0,20}(–≤–Њ—Б—Е–Є—Й|–≤–ї—О–±–Є–ї|–Ї—Л—А–≥—Л–Ј|–≤–Њ—Б—В–Њ—А–≥)|—В—Г—А–Є—Б—В.{0,20}(–≤–ї—О–±–Є–ї|–≤–Њ—Б—Е–Є—В|–≤–Њ—Б—В–Њ—А–≥|–Ї—А–∞—Б–Њ—В)|" +
            "(–Ї—А–∞—Б–Њ—В–∞|–њ—А–Є—А–Њ–і–∞|–≥–Њ—А—Л|–Є—Б—Б—Л–Ї-–Ї—Г–ї—М).{0,20}(–Є–љ–Њ—Б—В—А–∞–љ|—В—Г—А–Є—Б—В|–≤–Њ—Б—Е–Є—В|–њ–Њ—А–∞–Ј–Є–ї|—Г–і–Є–≤–Є–ї)|" +
            "—Б—З–∞—Б—В–ї–Є–≤|—А–∞–і–Њ—Б—В|–њ—А–∞–Ј–і–љ–Є–Ї|—О–±–Є–ї–µ–є.{0,10}(–Ї–≥|–±–Є—И–Ї–µ–Ї)|" +
            "–њ–Њ–Ј–Є—В–Є–≤|–≤–і–Њ—Е–љ–Њ–≤–ї—П|–Љ–Њ—В–Є–≤–Є—А|–і–Њ–±—А–Њ"
        ).containsMatchIn(t) -> "GOOD"
        // рЯУ∞ –Я—А–Њ–Є—Б—И–µ—Б—В–≤–Є—П, –Ї—А–Є–Љ–Є–љ–∞–ї, —Б–Њ–±—Л—В–Є—П вАФ –µ—Б–ї–Є –љ–µ –њ–Њ–њ–∞–ї–Њ –≤ –і—А—Г–≥–Є–µ —А—Г–±—А–Є–Ї–Є
        Regex(
            "–љ–∞–њ–∞–ї|–љ–∞–њ–∞–ї–Є|–∞—В–∞–Ї–Њ–≤–∞–ї|—Г–Ї—Г—Б|–Њ–≥—А–∞–±–Є–ї|—Г–Ї—А–∞–ї|–Ј–∞–і–µ—А–ґ–∞–ї|–∞—А–µ—Б—В|–Њ—Б—Г–і–Є–ї|–њ—А–Є–≥–Њ–≤–Њ—А|—Г–±–Є–є—Б—В–≤|—Г–±–Є–ї|" +
            "–њ–Њ–≥–Є–±|–ґ–µ—А—В–≤|–њ–Њ—Б—В—А–∞–і–∞–ї|—А–∞–љ–µ–љ|–≥–Њ—Б–њ–Є—В–∞–ї–Є–Ј|—Б–Ї–Њ—А—Г—О|–њ–Њ–ї–Є—Ж–Є|—Б–ї–µ–і—Б—В–≤–µ–љ|–њ—А–Њ–Ї—Г—А–Њ—А|—Б—Г–і|" +
            "–Љ–Њ—И–µ–љ–љ–Є–Ї|–Љ–Њ—И–µ–љ–љ–Є—З–µ—Б—В–≤|–Њ–±–≤–Є–љ–Є–ї–Є|–Њ–±–≤–Є–љ—П–µ–Љ|–њ—А–µ—Б—В—Г–њ–љ–Є–Ї|–њ—А–µ—Б—В—Г–њ–ї–µ–љ–Є|–Ї—А–∞–ґ–∞|—А–∞–Ј–±–Њ–є|" +
            "–∞–≤–∞—А–Є—П|–њ–Њ–ґ–∞—А|–≤–Ј—А—Л–≤|–Њ–±—А—Г—И–Є–ї|–љ–∞–≤–Њ–і–љ–µ–љ|–Ј–∞—В–Њ–њ–ї–µ–љ|—Н–≤–∞–Ї—Г–∞—Ж|—З–њ|—З—А–µ–Ј–≤—Л—З–∞–є–љ"
        ).containsMatchIn(t) -> "WORLD"
        else -> item.category
    }
    return if (cat != item.category) item.copy(category = cat) else item
}

/** –Э–Њ—А–Љ–∞–ї–Є–Ј—Г–µ—В –Ј–∞–≥–Њ–ї–Њ–≤–Њ–Ї –і–ї—П —Б—А–∞–≤–љ–µ–љ–Є—П: lowercase, –±–µ–Ј –Ј–љ–∞–Ї–Њ–≤, –њ–µ—А–≤—Л–µ 55 —Б–Є–Љ–≤–Њ–ї–Њ–≤ */
private fun titleKey(title: String): String =
    title.lowercase()
        .replace(Regex("[^a-z–∞-—П—С0-9 ]"), "")
        .replace(Regex("\\s+"), " ")
        .trim()
        .take(55)

/**
 * –Т—Л—З–Є—Б–ї—П–µ—В popularity-score –і–ї—П –љ–Њ–≤–Њ—Б—В–Є –љ–∞ –Њ—Б–љ–Њ–≤–µ —В—А—С—Е —Б–Є–≥–љ–∞–ї–Њ–≤:
 *
 * 1. sourceCount  вАФ —Б–Ї–Њ–ї—М–Ї–Њ –љ–µ–Ј–∞–≤–Є—Б–Є–Љ—Л—Е –Є—Б—В–Њ—З–љ–Є–Ї–Њ–≤ –љ–∞–њ–Є—Б–∞–ї–Є –Њ–± —Н—В–Њ–є —В–µ–Љ–µ.
 *                   3 –Є—Б—В–Њ—З–љ–Є–Ї–∞ –љ–∞–њ–Є—Б–∞–ї–Є –Њ–і–љ–Њ –Є —В–Њ –ґ–µ = —В–µ–Љ–∞ –≤–∞–ґ–љ–∞ –і–ї—П —А–µ–≥–Є–Њ–љ–∞.
 * 2. telegramViews вАФ —А–µ–∞–ї—М–љ—Л–µ –њ—А–Њ—Б–Љ–Њ—В—А—Л –≤ Telegram-–Ї–∞–љ–∞–ї–µ. 50K –њ—А–Њ—Б–Љ–Њ—В—А–Њ–≤ вЙ† —Б–ї—Г—З–∞–є–љ–∞—П –љ–Њ–≤–Њ—Б—В—М.
 * 3. isTrending    вАФ —В–µ–Љ–∞ —Б–Њ–≤–њ–∞–і–∞–µ—В —Б —В–µ–Љ —З—В–Њ –њ—А—П–Љ–Њ —Б–µ–є—З–∞—Б –Є—Й—Г—В –≤ Google KG/RU.
 *
 * –Ш—В–Њ–≥–Њ–≤—Л–є score –Є—Б–њ–Њ–ї—М–Ј—Г–µ—В—Б—П –њ—А–Є —Б–Њ—А—В–Є—А–Њ–≤–Ї–µ –≤–Љ–µ—Б—В–µ —Б –њ—А–Є–Њ—А–Є—В–µ—В–Њ–Љ –Є —Б–≤–µ–ґ–µ—Б—В—М—О.
 */
private fun popularityScore(item: NewsItem): Int {
    var score = 0
    // 1. Google Trends KG вАФ —Б–∞–Љ—Л–є —Б–Є–ї—М–љ—Л–є —Б–Є–≥–љ–∞–ї: —В–µ–Љ–∞ –∞–Ї—В—Г–∞–ї—М–љ–∞ –њ—А—П–Љ–Њ —Б–µ–є—З–∞—Б –≤ —А–µ–≥–Є–Њ–љ–µ
    if (item.isTrending) score += 60
    // 2. –Я—А–Њ—Б–Љ–Њ—В—А—Л Telegram вАФ —А–µ–∞–ї—М–љ—Л–є –Є–љ—В–µ—А–µ—Б –ї—О–і–µ–є: –ї–Њ–≥–∞—А–Є—Д–Љ–Є—З–µ—Б–Ї–∞—П —И–Ї–∞–ї–∞
    //    1KвЖТ10, 10KвЖТ20, 100KвЖТ30, 1MвЖТ40 –Њ—З–Ї–Њ–≤
    if (item.telegramViews > 0) {
        score += (Math.log10(item.telegramViews.toDouble()) * 10).toInt().coerceIn(0, 40)
    }
    // 3. –Ъ—А–Њ—Б—Б-–Є—Б—В–Њ—З–љ–Є–Ї–Њ–≤—Л–є —Б–Є–≥–љ–∞–ї: –Ї–∞–ґ–і—Л–є –і–Њ–њ–Њ–ї–љ–Є—В–µ–ї—М–љ—Л–є –Є—Б—В–Њ—З–љ–Є–Ї +20 –Њ—З–Ї–Њ–≤
    score += (item.sourceCount - 1) * 20
    return score
}

fun sortItems(items: List<NewsItem>, cat: String): List<NewsItem> {
    val maxAge = 30L * 24 * 60 * 60 * 1000  // 30 –і–љ–µ–є –Љ–∞–Ї—Б–Є–Љ—Г–Љ
    val now = System.currentTimeMillis()

    // –Ъ–∞—В–µ–≥–Њ—А–Є–Є –Ї–Њ—В–Њ—А—Л–µ –Э–Х –њ–Њ–Ї–∞–Ј—Л–≤–∞–µ–Љ –≤ –Њ–±—Й–µ–є –ї–µ–љ—В–µ (—Г –љ–Є—Е —Б–≤–Њ–Є –њ–ї–Є—В–Ї–Є)
    val excludedFromFeed = setOf("CURRENCY", "CRYPTO")

    val filtered = when (cat) {
        "ALL" -> items
            .filter { it.category !in excludedFromFeed }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
            .filter { it.title.length > 15 }
            .map { enrichCategory(it) }
        "URGENT" -> items
            .filter { it.category == "URGENT" || it.priority >= 3 }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
        else -> items
            .filter { it.category == cat }
            .filter { now - it.publishedAt < maxAge }
            .filter { !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
    }

    // вФАвФА –Ъ—А–Њ—Б—Б-–Є—Б—В–Њ—З–љ–Є–Ї–Њ–≤—Л–є —А–µ–є—В–Є–љ–≥ вФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФА
    // –°—З–Є—В–∞–µ–Љ —Б–Ї–Њ–ї—М–Ї–Њ —А–∞–Ј –Њ–і–љ–∞ —В–µ–Љ–∞ –≤—Б—В—А–µ—З–∞–µ—В—Б—П —Б—А–µ–і–Є —А–∞–Ј–љ—Л—Е –Є—Б—В–Њ—З–љ–Є–Ї–Њ–≤.
    // –Т–∞–ґ–љ–Њ –і–µ–ї–∞—В—М –Ф–Ю –і–µ–і—Г–њ–∞ вАФ –њ–Њ—Б–ї–µ –і–µ–і—Г–њ–∞ –Њ—Б—В–∞–љ–µ—В—Б—П —В–Њ–ї—М–Ї–Њ –Њ–і–љ–∞ –Ј–∞–њ–Є—Б—М.
    val titleCountMap = mutableMapOf<String, Int>()
    val titleViewsMap = mutableMapOf<String, Int>()  // —Б—Г–Љ–Љ–Є—А—Г–µ–Љ –њ—А–Њ—Б–Љ–Њ—В—А—Л –њ–Њ —В–µ–Љ–µ
    val titleTrendMap = mutableMapOf<String, Boolean>()
    filtered.forEach { item ->
        val tk = titleKey(item.title)
        titleCountMap[tk] = (titleCountMap[tk] ?: 0) + 1
        titleViewsMap[tk] = (titleViewsMap[tk] ?: 0) + item.telegramViews
        if (item.isTrending) titleTrendMap[tk] = true
    }

    // –Ю–±–Њ–≥–∞—Й–∞–µ–Љ items –∞–≥—А–µ–≥–Є—А–Њ–≤–∞–љ–љ—Л–Љ–Є –і–∞–љ–љ—Л–Љ–Є
    val enriched = filtered.map { item ->
        val tk = titleKey(item.title)
        val count   = titleCountMap[tk] ?: 1
        val views   = titleViewsMap[tk] ?: item.telegramViews
        val trend   = titleTrendMap[tk] ?: item.isTrending
        if (count != item.sourceCount || views != item.telegramViews || trend != item.isTrending)
            item.copy(sourceCount = count, telegramViews = views, isTrending = trend)
        else item
    }

    // –°–Њ—А—В–Є—А—Г–µ–Љ –њ–µ—А–µ–і –і–µ–і—Г–њ–Њ–Љ вАФ –ї—Г—З—И–Є–є —Н–Ї–Ј–µ–Љ–њ–ї—П—А (—Б —Д–Њ—В–Њ/–≤–Є–і–µ–Њ) –Њ—Б—В–∞—С—В—Б—П
    val preSorted = enriched.sortedWith(
        compareByDescending<NewsItem> {
            when {
                it.isVideo          -> 4
                it.imageUrl != null -> 3
                cat == "ALL" && it.category == "KG" -> 2
                else                -> 0
            }
        }.thenByDescending { it.priority * 1000L + it.publishedAt / 1000L }
    )

    // –Ф–µ–і—Г–њ–ї–Є–Ї–∞—Ж–Є—П: –њ–Њ URL, –њ–Њ—В–Њ–Љ –њ–Њ –љ–Њ—А–Љ–∞–ї–Є–Ј–Њ–≤–∞–љ–љ–Њ–Љ—Г –Ј–∞–≥–Њ–ї–Њ–≤–Ї—Г
    val seenUrls   = mutableSetOf<String>()
    val seenTitles = mutableSetOf<String>()
    val deduped = preSorted.filter { item ->
        val url = item.url.ifEmpty { item.title }
        val tk  = titleKey(item.title)
        if (url in seenUrls || (tk.length >= 30 && tk in seenTitles)) return@filter false
        seenUrls   += url
        if (tk.length >= 30) seenTitles += tk
        true
    }

    // вФАвФА –§–Є–љ–∞–ї—М–љ–∞—П —Б–Њ—А—В–Є—А–Њ–≤–Ї–∞ —Б popularity score вФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФА
    val sorted = deduped.sortedWith(
        compareByDescending<NewsItem> {
            // –Я—А–Њ—З–Є—В–∞–љ–љ—Л–µ –≤—Б–µ–≥–і–∞ –≤–љ–Є–Ј—Г (Instagram-–ї–Њ–≥–Є–Ї–∞)
            if (DataBridge.isSeen(it.url)) return@compareByDescending Long.MIN_VALUE
            // –С–∞–Ј–Њ–≤—Л–є score: –њ—А–Є–Њ—А–Є—В–µ—В + —Б–≤–µ–ґ–µ—Б—В—М (–≤ —З–∞—Б–∞—Е, max 72h) + popularity
            val freshness = ((72 * 60 * 60 * 1000L - (now - it.publishedAt).coerceAtLeast(0L)) / 3_600_000L).coerceIn(0L, 72L)
            val pop  = popularityScore(it).toLong()
            val base = when {
                cat == "ALL" && it.category == "KG"   -> 200L
                cat == "ALL" && it.category == "GOOD" -> 120L  // —Е–Њ—А–Њ—И–Є–µ –љ–Њ–≤–Њ—Б—В–Є –њ–Њ–і–љ–Є–Љ–∞–µ–Љ вАФ –±–∞–ї–∞–љ—Б –љ–∞—Б—В—А–Њ–µ–љ–Є—П
                cat == "ALL" && it.category == "TOURS"-> 80L   // —В—Г—А–Є–Ј–Љ/–≤–і–Њ—Е–љ–Њ–≤–µ–љ–Є–µ —В–Њ–ґ–µ –≤–≤–µ—А—Е
                it.isVideo          -> 150L
                it.imageUrl != null -> 100L
                else                -> 0L
            }
            // –Ш—В–Њ–≥–Њ–≤—Л–є —А–∞–љ–≥: priority (0-3) * 1000 + –њ–Њ–њ—Г–ї—П—А–љ–Њ—Б—В—М + —Б–≤–µ–ґ–µ—Б—В—М + —В–Є–њ
            it.priority * 1000L + pop + freshness * 2 + base
        }
    )

    // –Ю–≥—А–∞–љ–Є—З–Є–≤–∞–µ–Љ –≤–Є–і–µ–Њ: –љ–µ –±–Њ–ї–µ–µ 2 –≤ –ї–µ–љ—В–µ, –љ–µ –±–Њ–ї–µ–µ 1 –љ–∞ —В–µ–Љ—Г
    // –Ґ–µ–Љ–∞ –Њ–њ—А–µ–і–µ–ї—П–µ—В—Б—П –њ–Њ –њ–µ—А–µ—Б–µ—З–µ–љ–Є—О –Ј–љ–∞—З–Є–Љ—Л—Е —Б–ї–Њ–≤ (>4 –±—Г–Ї–≤) вАФ –ї–Њ–≤–Є—В "–І–µ–ї–Њ–≤–µ–Ї-–њ–∞—Г–Ї" –≤–Њ –≤—Б–µ—Е –≤–∞—А–Є–∞–љ—В–∞—Е
    val finalList = if (cat == "ALL" || cat == "URGENT") {
        val seenTopicWords = mutableSetOf<String>()
        var videoCount = 0
        sorted.filter { item ->
            if (!item.isVideo) return@filter true
            if (videoCount >= 2) return@filter false
            val words = item.title.lowercase()
                .split(Regex("[\\s\\-:,.!?\"']+"))
                .filter { it.length > 4 }
                .toSet()
            if (words.any { it in seenTopicWords }) return@filter false
            seenTopicWords += words
            videoCount++
            true
        }
    } else sorted

    return if (cat == "ALL") interleave(finalList) else finalList
}

/** –Я–µ—А–µ–Љ–µ—И–Є–≤–∞–µ—В —Б–њ–Є—Б–Њ–Ї —З—В–Њ–±—Л –Њ–і–Є–љ–∞–Ї–Њ–≤—Л–µ –Є—Б—В–Њ—З–љ–Є–Ї–Є/–Ї–∞—В–µ–≥–Њ—А–Є–Є –љ–µ —И–ї–Є –њ–Њ–і—А—П–і */
private fun interleave(items: List<NewsItem>): List<NewsItem> {
    // –У—А—Г–њ–њ–Є—А—Г–µ–Љ –њ–Њ –Ї–∞—В–µ–≥–Њ—А–Є–Є
    val byCategory = items.groupBy { it.category }.values.map { it.toMutableList() }
    val result = mutableListOf<NewsItem>()
    var hasMore = true
    while (hasMore) {
        hasMore = false
        for (group in byCategory) {
            if (group.isNotEmpty()) {
                result.add(group.removeAt(0))
                hasMore = true
            }
        }
    }
    return result
}

