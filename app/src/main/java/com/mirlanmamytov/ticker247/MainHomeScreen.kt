fun timeAgo(timestamp: Long): String {
    val diff = System.currentTimeMillis() - timestamp
    val min  = diff / 60_000
    val h    = min / 60
    return when {
        min < 1  -> "СӮРҫР»СҢРәРҫ СҮСӮРҫ"
        min < 60 -> "$min РјРёРҪ"
        h < 24   -> "$h СҮ"
        else     -> "${h / 24} РҙРҪ"
    }
}

