fun timeAgo(timestamp: Long): String {
    val diff = System.currentTimeMillis() - timestamp
    val min  = diff / 60_000
    val h    = min / 60
    return when {
        min < 1  -> "Đéđżđ╗Đîđ║đż ĐçĐéđż"
        min < 60 -> "$min đ╝đŞđŻ"
        h < 24   -> "$h Đç"
        else     -> "${h / 24} đ┤đŻ"
    }
}

