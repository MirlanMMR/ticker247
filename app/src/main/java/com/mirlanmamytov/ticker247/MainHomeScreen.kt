/** РқРҫСҖРјР°Р»РёР·СғРөСӮ Р·Р°РіРҫР»РҫРІРҫРә РҙР»СҸ СҒСҖР°РІРҪРөРҪРёСҸ: lowercase, РұРөР· Р·РҪР°РәРҫРІ, РҝРөСҖРІСӢРө 55 СҒРёРјРІРҫР»РҫРІ */
private fun titleKey(title: String): String =
    title.lowercase()
        .replace(Regex("[^a-zР°-СҸС‘0-9 ]"), "")
        .replace(Regex("\\s+"), " ")
        .trim()
        .take(55)

/**
 * Р’СӢСҮРёСҒР»СҸРөСӮ popularity-score РҙР»СҸ РҪРҫРІРҫСҒСӮРё РҪР° РҫСҒРҪРҫРІРө СӮСҖС‘С… СҒРёРіРҪР°Р»РҫРІ:
 *
 * 1. sourceCount  вҖ” СҒРәРҫР»СҢРәРҫ РҪРөР·Р°РІРёСҒРёРјСӢС… РёСҒСӮРҫСҮРҪРёРәРҫРІ РҪР°РҝРёСҒР°Р»Рё РҫРұ СҚСӮРҫР№ СӮРөРјРө.
 *                   3 РёСҒСӮРҫСҮРҪРёРәР° РҪР°РҝРёСҒР°Р»Рё РҫРҙРҪРҫ Рё СӮРҫ Р¶Рө = СӮРөРјР° РІР°Р¶РҪР° РҙР»СҸ СҖРөРіРёРҫРҪР°.
 * 2. telegramViews вҖ” СҖРөР°Р»СҢРҪСӢРө РҝСҖРҫСҒРјРҫСӮСҖСӢ РІ Telegram-РәР°РҪР°Р»Рө. 50K РҝСҖРҫСҒРјРҫСӮСҖРҫРІ вү  СҒР»СғСҮР°Р№РҪР°СҸ РҪРҫРІРҫСҒСӮСҢ.
 * 3. isTrending    вҖ” СӮРөРјР° СҒРҫРІРҝР°РҙР°РөСӮ СҒ СӮРөРј СҮСӮРҫ РҝСҖСҸРјРҫ СҒРөР№СҮР°СҒ РёСүСғСӮ РІ Google KG/RU.
 *
 * РҳСӮРҫРіРҫРІСӢР№ score РёСҒРҝРҫР»СҢР·СғРөСӮСҒСҸ РҝСҖРё СҒРҫСҖСӮРёСҖРҫРІРәРө РІРјРөСҒСӮРө СҒ РҝСҖРёРҫСҖРёСӮРөСӮРҫРј Рё СҒРІРөР¶РөСҒСӮСҢСҺ.
 */
private fun popularityScore(item: NewsItem): Int {
    var score = 0
    // 1. Google Trends KG вҖ” СҒР°РјСӢР№ СҒРёР»СҢРҪСӢР№ СҒРёРіРҪР°Р»: СӮРөРјР° Р°РәСӮСғР°Р»СҢРҪР° РҝСҖСҸРјРҫ СҒРөР№СҮР°СҒ РІ СҖРөРіРёРҫРҪРө
    if (item.isTrending) score += 60
    // 2. РҹСҖРҫСҒРјРҫСӮСҖСӢ Telegram вҖ” СҖРөР°Р»СҢРҪСӢР№ РёРҪСӮРөСҖРөСҒ Р»СҺРҙРөР№: Р»РҫРіР°СҖРёС„РјРёСҮРөСҒРәР°СҸ СҲРәР°Р»Р°
    //    1KвҶ’10, 10KвҶ’20, 100KвҶ’30, 1MвҶ’40 РҫСҮРәРҫРІ
    if (item.telegramViews > 0) {
        score += (Math.log10(item.telegramViews.toDouble()) * 10).toInt().coerceIn(0, 40)
    }
    // 3. РҡСҖРҫСҒСҒ-РёСҒСӮРҫСҮРҪРёРәРҫРІСӢР№ СҒРёРіРҪР°Р»: РәР°Р¶РҙСӢР№ РҙРҫРҝРҫР»РҪРёСӮРөР»СҢРҪСӢР№ РёСҒСӮРҫСҮРҪРёРә +20 РҫСҮРәРҫРІ
    score += (item.sourceCount - 1) * 20
    return score
}

fun sortItems(items: List<NewsItem>, cat: String): List<NewsItem> {
    val maxAge = 30L * 24 * 60 * 60 * 1000  // 30 РҙРҪРөР№ РјР°РәСҒРёРјСғРј
    val now = System.currentTimeMillis()

    // РҡР°СӮРөРіРҫСҖРёРё РәРҫСӮРҫСҖСӢРө РқР• РҝРҫРәР°Р·СӢРІР°РөРј РІ РҫРұСүРөР№ Р»РөРҪСӮРө (Сғ РҪРёС… СҒРІРҫРё РҝР»РёСӮРәРё)
