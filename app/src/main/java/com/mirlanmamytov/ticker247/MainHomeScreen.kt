    items: List<NewsItem>,
    textColor: Color,
    subColor: Color,
    shuffledPatterns: List<List<TileSize>>,
    onOpenTikTok: (List<NewsItem>, Int) -> Unit,
    adOffset: Int = 0
) {
    Column(Modifier.fillMaxWidth().padding(horizontal = 10.dp)) {
