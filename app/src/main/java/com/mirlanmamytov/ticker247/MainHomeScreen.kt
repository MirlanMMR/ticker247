@Composable
fun NewsTileGrid(
    category: String = "ALL",
    textColor: Color,
    subColor: Color,
    onOpenTikTok: (List<NewsItem>, Int) -> Unit
) {
    val allItems by DataBridge.newsItemsFlow.collectAsState()
    val sorted = remember(allItems, category) {
        // РҳСҒРәР»СҺСҮР°РөРј РҪРҫРІРҫСҒСӮРё, СғР¶Рө РҝРҫРәР°Р·Р°РҪРҪСӢРө РІ hero-РәР°СҖСғСҒРөР»Рё (СӮРҫР»СҢРәРҫ РҙР»СҸ РіР»Р°РІРҪРҫР№)
        val heroUrls = if (category == "ALL")
            selectHeroItems(allItems).map { it.url.ifEmpty { it.title } }.toSet()
        else emptySet()
        sortItems(allItems, category).filter { (it.url.ifEmpty { it.title }) !in heroUrls }
    }

    if (allItems.isEmpty()) return  // HomeContent СғР¶Рө РҝРҫРәР°Р·Р°Р» SplashLoadingScreen

    // РЎР»СғСҮР°Р№РҪСӢР№ РҝРҫСҖСҸРҙРҫРә РҝР°СӮСӮРөСҖРҪРҫРІ вҖ” seed РҫСӮ РҙРҪСҸ, СҒСӮР°РұРёР»РөРҪ РІ СҖР°РјРәР°С… РҙРҪСҸ
    val shuffledPatterns = remember {
        val daySeed = System.currentTimeMillis() / (24 * 60 * 60 * 1000)
        TILE_PATTERNS.shuffled(java.util.Random(daySeed))
    }

    // Р”Р»СҸ РІРәР»Р°РҙРәРё ALL вҖ” РҝРҫРәР°Р·СӢРІР°РөРј РҙРІРө СҒРөРәСҶРёРё: РңРөСҒСӮРҪСӢРө Рё РңРёСҖРҫРІСӢРө
    // YouTube/VIRAL РұРөР· scope РёРҙСғСӮ РІ local (РІРёСҖСғСҒСӢ СҖРөРіРёРҫРҪР°Р»СҢРҪСӢРө РҝРҫ РҝСҖРёСҖРҫРҙРө)
    if (category == "ALL") {
        val localItems = sorted.filter { it.scope == "local" || (it.scope == "world" && it.isVideo) }
        val worldItems = sorted.filter { it.scope == "world" && !it.isVideo }
        Column(Modifier.fillMaxWidth()) {
            if (localItems.isNotEmpty()) {
                ScopeSectionHeader(emoji = "рҹ“Қ", title = "РңРөСҒСӮРҪСӢРө")
                NewsTileBlock(localItems, textColor, subColor, shuffledPatterns, onOpenTikTok, adOffset = 0)
            }
            if (worldItems.isNotEmpty()) {
                ScopeSectionHeader(emoji = "рҹҢҚ", title = "РңРёСҖРҫРІСӢРө")
                NewsTileBlock(worldItems, textColor, subColor, shuffledPatterns, onOpenTikTok, adOffset = 3)
            }
        }
        return
    }

    Column(Modifier.fillMaxWidth().padding(horizontal = 10.dp)) {
        var itemIdx = 0
        var rowCount = 0
        while (itemIdx < sorted.size) {
            // Р СҸРҙ 4: РҝРөСҖРІСӢР№ СҒР»РҫСӮ РәРҫРҪСӮРөРәСҒСӮРҪРҫР№ СҖРөРәР»Р°РјСӢ (AdMob)
            if (rowCount == 4) {
                ContextualAdSlot()
                Spacer(Modifier.height(8.dp))
            }
