// вФАвФАвФА –Ц–Є–≤–∞—П –њ–ї–Є—В–Ї–∞ —Б Lumia flip-–∞–љ–Є–Љ–∞—Ж–Є–µ–є вФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФАвФА

@OptIn(androidx.compose.animation.ExperimentalAnimationApi::class)
@Composable
fun LiveTile(
    item: NewsItem,
    size: TileSize,
    textColor: Color,
    subColor: Color,
    onClick: () -> Unit
) {
    val style    = newsItemStyle(item.category)
    val isSeen   = DataBridge.isSeen(item.url)   // –њ—А–Њ—З–Є—В–∞–љ–љ—Л–µ –њ—А–Є–≥–ї—Г—И–∞–µ–Љ
    // og:image –њ–Њ–і–≥—А—Г–ґ–∞–µ—В—Б—П –ї–µ–љ–Є–≤–Њ –µ—Б–ї–Є –≤ RSS —Д–Њ—В–Њ –љ–µ –±—Л–ї–Њ
    var resolvedImageUrl by remember(item.url) { mutableStateOf(item.imageUrl) }
    var showPhoto by remember(item.url) { mutableStateOf(false) }
    val flipInterval = remember(item.url) { (3500L..9000L).random() }

    // –§–Њ–љ–Њ–≤–∞—П –Ј–∞–≥—А—Г–Ј–Ї–∞ —Д–Њ—В–Њ: og:image вЖТ Wikipedia вЖТ –Ї–∞—В–µ–≥–Њ—А–Є–є–љ—Л–є fallback
    LaunchedEffect(item.url) {
