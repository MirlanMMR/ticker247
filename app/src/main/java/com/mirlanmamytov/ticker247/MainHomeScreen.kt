// РһСӮРұРҫСҖ РҪРҫРІРҫСҒСӮРөР№ РҙР»СҸ hero-РәР°СҖСғСҒРөР»Рё вҖ” РөРҙРёРҪР°СҸ С„СғРҪРәСҶРёСҸ, СҮСӮРҫРұСӢ СҒРөСӮРәР° РҝР»РёСӮРҫРә РјРҫРіР»Р°
// РёСҒРәР»СҺСҮРёСӮСҢ СҚСӮРё Р¶Рө РҪРҫРІРҫСҒСӮРё Рё РҪРө РҝРҫРәР°Р·СӢРІР°СӮСҢ РёС… РІСӮРҫСҖРҫР№ СҖР°Р·
fun selectHeroItems(allItems: List<NewsItem>): List<NewsItem> {
    val isNews = { it: NewsItem -> it.category !in setOf("CURRENCY", "CRYPTO") && it.cryptoSymbol == null }
    val notSpam = { it: NewsItem -> !SPAM_PATTERNS_STATIC.containsMatchIn(it.title) && !RemoteConfig.buildSpamRegex().containsMatchIn(it.title) }
    val candidates = (allItems.filter { isNews(it) && notSpam(it) && it.isEditorCarousel } +
     allItems.filter { isNews(it) && notSpam(it) && (it.priority >= 2 || it.category == "URGENT") } +
     allItems.filter { isNews(it) && notSpam(it) && it.imageUrl != null && !it.isVideo })
        .distinctBy { it.url.ifEmpty { it.title } }
    // РқРө РұРҫР»РөРө 1 РІРёРҙРөРҫ РІ РәР°СҖСғСҒРөР»Рё, СӮРөРјР°-РҙРөРҙСғРҝ
    val seenWords = mutableSetOf<String>()
    var videoCount = 0
    return candidates.filter { item ->
        val words = item.title.lowercase().split(Regex("[\\s\\-:,.!?\"']+")).filter { it.length > 4 }.toSet()
        if (item.isVideo) {
            if (videoCount >= 1) return@filter false
            if (words.any { it in seenWords }) return@filter false
            videoCount++
        }
        seenWords += words
        true
    }.take(10)
}

@OptIn(androidx.compose.foundation.ExperimentalFoundationApi::class)
@Composable
fun HeroCarousel(onOpenTikTok: (List<NewsItem>, Int) -> Unit) {
    val allItems by DataBridge.newsItemsFlow.collectAsState()

    // РһСӮРұРёСҖР°РөРј: РҝСҖРёРҫСҖРёСӮРөСӮ 2+ РёР»Рё РөСҒСӮСҢ С„РҫСӮРҫ вҖ” СӮРҫР»СҢРәРҫ РҪРҫРІРҫСҒСӮРё, РұРөР· РәСҖРёРҝСӮСӢ/РІР°Р»СҺСӮСӢ/СҒРҝР°РјР°
    val heroItems = remember(allItems) { selectHeroItems(allItems) }

    if (heroItems.isEmpty()) return

    val pagerState = androidx.compose.foundation.pager.rememberPagerState(pageCount = { heroItems.size })
    val coroutineScope = rememberCoroutineScope()

    // РҗРІСӮРҫРҝСҖРҫРәСҖСғСӮРәР° РәР°Р¶РҙСӢРө 5 СҒРөРәСғРҪРҙ
    LaunchedEffect(pagerState) {
        while (isActive) {
            kotlinx.coroutines.delay(5000)
            val next = (pagerState.currentPage + 1) % heroItems.size
            try { pagerState.animateScrollToPage(next) } catch (_: Exception) {}
        }
    }

    Column(Modifier.fillMaxWidth().padding(bottom = 12.dp)) {
        // РҹРөР№РҙР¶РөСҖ вҖ” С„РёРәСҒРёСҖРҫРІР°РҪРҪР°СҸ РІСӢСҒРҫСӮР° 240dp (РҪРёРәРҫРіРҙР° РҪРө РәСҖР°СҲРёСӮ РІ LazyColumn)
        Box(Modifier.fillMaxWidth().height(240.dp)) {
            androidx.compose.foundation.pager.HorizontalPager(
                state = pagerState,
                modifier = Modifier.fillMaxSize(),
                pageSpacing = 0.dp
            ) { page ->
                val item = heroItems[page]
                HeroCard(
                    item = item,
                    onClick = { onOpenTikTok(heroItems, page) }
                )
            }
        }

        // РўРҫСҮРәРё-РёРҪРҙРёРәР°СӮРҫСҖСӢ
        Row(
            Modifier.fillMaxWidth().padding(top = 10.dp),
            horizontalArrangement = Arrangement.Center,
            verticalAlignment = Alignment.CenterVertically
        ) {
            heroItems.forEachIndexed { i, _ ->
                val isSelected = i == pagerState.currentPage
                val dotSize by androidx.compose.animation.core.animateDpAsState(
                    targetValue = if (isSelected) 20.dp else 6.dp,
                    animationSpec = spring(dampingRatio = Spring.DampingRatioMediumBouncy),
                    label = "dot_$i"
                )
                Box(
                    Modifier
                        .padding(horizontal = 2.dp)
                        .height(6.dp).width(dotSize)
                        .clip(RoundedCornerShape(50))
                        .background(
                            if (isSelected) Color(0xFF6366F1) else Color(0xFFCBD5E1)
                        )
                )
            }
        }
    }
}

