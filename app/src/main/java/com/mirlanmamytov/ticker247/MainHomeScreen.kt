        Regex(
            "–ї–∞–є—Д—Е–∞–Ї|—Б–Њ–≤–µ—В|–Ї–∞–Ї (—Б–і–µ–ї–∞—В—М|—Г–±—А–∞—В—М|–њ–Њ—З–Є–љ–Є—В—М|—Б—Н–Ї–Њ–љ–Њ–Љ–Є—В—М|–њ—А–Є–≥–Њ—В–Њ–≤–Є—В—М|–≤—Л–±—А–∞—В—М)|" +
            "—В–Њ–њ.{0,5}(—Б–Њ–≤–µ—В–Њ–≤|—Б–њ–Њ—Б–Њ–±–Њ–≤|–њ—А–∞–≤–Є–ї|–ї–∞–є—Д—Е–∞–Ї)|" +
            "—А–µ—Ж–µ–њ—В|–≥–Њ—В–Њ–≤–Є–Љ|–±–ї—О–і–Њ|–Ї—Г—Е–љ|–≤–Ї—Г—Б–љ–Њ|–±—Л—Б—В—А–Њ –њ—А–Є–≥–Њ—В–Њ–≤|" +
            "—Г–±–Њ—А–Ї|—З–Є—Б—В–Њ—В|–њ–Њ—А—П–і–Њ–Ї –і–Њ–Љ–∞|–Њ—А–≥–∞–љ–Є–Ј.{0,10}(–і–Њ–Љ|–њ—А–Њ—Б—В—А–∞–љ—Б—В–≤)|" +
            "–Њ—В–љ–Њ—И–µ–љ–Є.{0,15}(—Б–Њ–≤–µ—В|–њ—Б–Є—Е–Њ–ї–Њ–≥|–њ–∞—А–∞|—Б–µ–Љ—М)|" +
            "–≤–Њ—Б–њ–Є—В–∞–љ–Є.{0,10}(–і–µ—В–µ–є|—А–µ–±—С–љ–Ї)|–і–µ—В–Є|—А–µ–±—С–љ–Њ–Ї.{0,15}(—А–∞–Ј–≤–Є—В–Є|–Ј–і–Њ—А–Њ–≤—М)|" +
            "—Н–Ї–Њ–љ–Њ–Љ–Є.{0,15}(–і–µ–љ—М–≥–Є|—Б–≤–µ—В|–≤–Њ–і—Г|–≥–∞–Ј)|—Г–Љ–љ—Л–є –і–Њ–Љ|" +
